const cities = ["Paris", "London", "Rome", "Paris"];

// Find first index of "Paris"
console.log("🔍 First Paris:", cities.indexOf("Paris")); // 0

// Find last index of "Paris"
console.log("🔁 Last Paris:", cities.lastIndexOf("Paris")); // 3

// Check if "Rome" exists
console.log("🗺️ Has Rome?", cities.includes("Rome")); // true
