const tools = ["hammer", "screwdriver", "wrench"];

// Add to end
tools.push("drill");
console.log("🔧 After push:", tools);

// Remove from end
tools.pop();
console.log("🔙 After pop:", tools);

// Add to beginning
tools.unshift("saw");
console.log("🏁 After unshift:", tools);

// Remove from beginning
tools.shift();
console.log("🏃 After shift:", tools);
