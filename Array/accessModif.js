const colors = ["red", "green", "blue"];

// Access elements by index (starting from 0)
console.log("🎨 First color:", colors[0]); // red

// Modify elements
colors[1] = "yellow";
console.log("🖌️ Modified colors:", colors); // ["red", "yellow", "blue"]

// Add a new element
colors[3] = "purple";
console.log("➕ Added color:", colors); // ["red", "yellow", "blue", "purple"]
